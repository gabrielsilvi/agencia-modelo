      <h2 class="text-center">Agência de viagens utilizando mecanismo de inteligência artificial para sistema de recomendação</h2>
      <p class="text-justify">&emsp;A princípio o sistema busca sugerir pacotes de viagens para lugares conforme o histórico de viagens 
        dos usuários, isso será realizado utilizando inteligência artificial. Quando o usuário estiver realizando cadastro no sistema 
        será dado ao mesmo opções de que ele espera ter para suas viagens(lugares quentes, frios, natureza, urbanismo, pouco de tudo...). 
        Com essa informações o sistema irá adaptar-se para mostrar pacotes de viagens de acordo com suas preferências. Ele também levará 
        em consideração os pacotes que o usuário já escolheu e também aqueles que são mais adquiridos por outros outros usuários.</p>
